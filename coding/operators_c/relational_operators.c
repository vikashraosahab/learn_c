/*
 *********** RELATIONAL OPERATORS *************
 ****** > >= < <= != == **********
*/
#include <stdio.h>

int main (void) {
     int a = 6;
     int b = 10;

     printf("%d\n",a > b);
     printf("%d\n",a >= b);
     printf("%d\n",a < b);
     printf("%d\n",a <= b);
     printf("%d\n",a == b);
     printf("%d\n",a != b);
     
    return 0;
}